import {
  Edited as EditedEvent,
  Locked as LockedEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent,
  Submitted as SubmittedEvent,
  Unlocked as UnlockedEvent
} from "../generated/Questions/Questions"
import {
  Edited,
  Locked,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked,
  Submitted,
  Unlocked
} from "../generated/schema"

export function handleEdited(event: EditedEvent): void {
  let entity = new Edited(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.tokenId = event.params.tokenId
  entity.question = event.params.question

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

